    private void inActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inActionPerformed
        try {
            XSSFWorkbook workbook = new XSSFWorkbook();
            XSSFSheet spreadsheet = workbook.createSheet("DoiTac");

            XSSFRow row = null;
            Cell cell = null;

            row = spreadsheet.createRow(0);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("DANH SÁCH Đối Tác");

            row = spreadsheet.createRow(1);
            row.setHeight((short) 500);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("STT");
            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue("Mã Đối Tác");
            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue("Tên Chiến Dịch");
            cell = row.createCell(3, CellType.STRING);
            cell.setCellValue("Ngày Bắt Đầu");
            cell = row.createCell(4, CellType.STRING);
            cell.setCellValue("Ngày Kết Thúc");
            cell = row.createCell(5, CellType.STRING);
            cell.setCellValue("Chi Phí Quảng Cáo");

            Connection con = ConDB.ketnoiDB();
            String sql = "SELECT * FROM DoiTac";
            PreparedStatement st = con.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            int i = 0;
            while (rs.next()) {
                row = spreadsheet.createRow(2 + i);
                row.setHeight((short) 400);

                row.createCell(0).setCellValue(i + 1); 
                row.createCell(1).setCellValue(rs.getString("Madoitac"));
                row.createCell(2).setCellValue(rs.getString("Tenquangcao"));

                Date ngayBatDau = new Date(rs.getDate("Ngaybatdau").getTime());
                Date ngayKetThuc = new Date(rs.getDate("Ngayketthuc").getTime());

                CreationHelper createHelper = workbook.getCreationHelper();
                CellStyle cellStyle = workbook.createCellStyle();
                cellStyle.setDataFormat(createHelper.createDataFormat().getFormat("dd/MM/yyyy"));

                cell = row.createCell(3);
                cell.setCellValue(ngayBatDau);
                cell.setCellStyle(cellStyle);

                cell = row.createCell(4);
                cell.setCellValue(ngayKetThuc);
                cell.setCellStyle(cellStyle);

                row.createCell(5).setCellValue(rs.getInt("Chiphi"));

                i++;
            }

            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Lưu file Excel");

            int userSelection = fileChooser.showSaveDialog(null);
            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File fileToSave = fileChooser.getSelectedFile();
                String filePath = fileToSave.getAbsolutePath();
                if (!filePath.endsWith(".xlsx")) {
                    filePath += ".xlsx";
                }
                FileOutputStream fileOut = new FileOutputStream(filePath);
                workbook.write(fileOut);
                fileOut.close();
                JOptionPane.showMessageDialog(this, "Xuất dữ liệu ra Excel thành công!");
            }

            rs.close();
            st.close();
            con.close();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi xuất dữ liệu ra Excel.");
        }
    }//GEN-LAST:event_inActionPerformed

