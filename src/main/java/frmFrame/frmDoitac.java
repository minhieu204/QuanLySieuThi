         private void loadTable() {
    try {
        Connection con = ConDB.ketnoiDB();
        Statement st = con.createStatement();

      
        java.sql.Date currentDate = new java.sql.Date(System.currentTimeMillis());

        
        String sql = "SELECT * FROM DoiTac";
        ResultSet rs = st.executeQuery(sql);

        DefaultTableModel model = new DefaultTableModel(new String[]{"Mã đối tác", "Tên quảng cáo",  "Ngày bắt đầu", "Ngày kết thúc", "Chi phí"}, 0);

        while (rs.next()) {
            Vector<Object> v = new Vector<>();
            v.add(rs.getString("Madoitac"));
            v.add(rs.getString("Tenquangcao"));
            v.add(rs.getDate("Ngaybatdau").toString());
            v.add(rs.getDate("Ngayketthuc").toString());
            v.add(rs.getInt("Chiphi"));

           
            java.sql.Date ngayBatDau = rs.getDate("Ngaybatdau");
            java.sql.Date ngayKetThuc = rs.getDate("Ngayketthuc");

            model.addRow(v);
        }

        TableDT.setModel(model);
        con.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Lỗi khi tải dữ liệu!");
    }
}
    
    
