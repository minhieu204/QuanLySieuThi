    private void TableDTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableDTMouseClicked
        int i = TableDT.getSelectedRow(); 
        DefaultTableModel model = (DefaultTableModel) TableDT.getModel();


        MaDT.setText(model.getValueAt(i, 0).toString()); 
        TenCD.setText(model.getValueAt(i, 1).toString()); 

        try {
            java.util.Date ngayBatDau = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(i, 2).toString());
            DateS.setDate(ngayBatDau);
        } catch (Exception e) {
            e.printStackTrace();
        }

        try {
            java.util.Date ngayKetThuc = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(i, 3).toString());
            DateE.setDate(ngayKetThuc); 
        } catch (Exception e) {
            e.printStackTrace();
        }

        PhiQC.setText(model.getValueAt(i, 4).toString()); 

        MaDT.setEnabled(false);
        them.setEnabled(false); 
        in.setEnabled(false); 
    }//GEN-LAST:event_TableDTMouseClicked

